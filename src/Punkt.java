public class Punkt {

    // Deklarer int x og y datatyper
    private int x, y;

    // En constructor der sÃ¦tter x og y
    public Punkt(int x, int y) {
        this.x = x;
        this.y = y;
    }

    // Getters til x og y
    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
